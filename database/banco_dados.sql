('-- VIEW 1: Ranking semanal de XP (gamificação)'),
('-- UC: Dashboard do Aluno — Visualiza Progresso'),
('-- ============================================================'),
('CREATE OR REPLACE VIEW vw_ranking_semanal AS'),
('SELECT'),
('    u.id,'),
('    u.nome,'),
('    u.foto_url,'),
('    u.nivel,'),
('    COALESCE(SUM(m.pontos_xp) FILTER ('),
('        WHERE p.data_conclusao >= date_trunc('week', NOW())'),
('    ), 0)                                AS xp_semana,'),
('    u.xp_total,'),
('    RANK() OVER (ORDER BY'),
('        COALESCE(SUM(m.pontos_xp) FILTER ('),
('            WHERE p.data_conclusao >= date_trunc('week', NOW())'),
('        ), 0) DESC'),
('    )                                    AS posicao'),
('FROM usuario u'),
('LEFT JOIN progresso p ON p.aluno_id = u.id'),
('LEFT JOIN modulo m    ON m.id = p.modulo_id'),
('WHERE u.tipo_perfil = 'aluno''),
('  AND u.ativo = TRUE'),
('GROUP BY u.id, u.nome, u.foto_url, u.nivel, u.xp_total;'),
('-- ============================================================'),
('-- VIEW 2: Dashboard do mentor (alunos + progresso)'),
('-- UC: Acompanha Alunos Mentorados'),
('-- ============================================================'),
('CREATE OR REPLACE VIEW vw_dashboard_mentor AS'),
('SELECT'),
('    me.mentor_id,'),
('    me.aluno_id,'),
('    u.nome          AS nome_aluno,'),
('    u.nivel,'),
('    u.xp_total,'),
('    me.status       AS status_mentoria,'),
('    me.data_solicitacao,'),
('    COUNT(p.id)     AS modulos_concluidos'),
('FROM mentoria me'),
('JOIN  usuario u     ON u.id  = me.aluno_id'),
('LEFT JOIN progresso p ON p.aluno_id = me.aluno_id'),
('WHERE me.status = 'aceita''),
('GROUP BY me.mentor_id, me.aluno_id, u.nome, u.nivel,'),
('         u.xp_total, me.status, me.data_solicitacao;'),
('-- ============================================================'),
('-- VIEW 3: Estatísticas de uso da IA por tipo de mídia'),
('-- UC: Visualiza Relatórios e Métricas (Admin)'),
('-- ============================================================'),
('CREATE OR REPLACE VIEW vw_uso_ia AS'),
('SELECT'),
('    tipo_midia,'),
('    COUNT(*)                          AS total_interacoes,'),
('    ROUND(AVG(avaliacao)::NUMERIC, 2) AS avaliacao_media,'),
('    SUM(tokens_usados)                AS tokens_totais,'),
('    date_trunc('day', criado_em)      AS dia'),
('FROM ia_interacao'),
('GROUP BY tipo_midia, date_trunc('day', criado_em)'),
('ORDER BY dia DESC;'),
('-- ============================================================'),
('-- VIEW 4: Perguntas via foto ou áudio (para auditoria IA)'),
