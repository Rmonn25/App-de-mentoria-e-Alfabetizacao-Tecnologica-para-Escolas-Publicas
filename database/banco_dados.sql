('    u.nome         AS usuario_nome,'),
('    u.tipo_perfil,'),
('    i.tipo_midia,'),
('    a.url_storage  AS arquivo_url,'),
('    a.transcricao  AS audio_transcrito,'),
('    i.pergunta,'),
('    i.resposta_ia,'),
('    i.avaliacao,'),
('    i.criado_em'),
('FROM ia_interacao i'),
('JOIN usuario u      ON u.id = i.usuario_id'),
('LEFT JOIN arquivo_midia a ON a.id = i.arquivo_id'),
('WHERE i.tipo_midia IN ('foto', 'audio')'),
